const fs = require('fs');
const path = require('path');

const WORKSPACE_DIR = '/Users/matteoromano/Desktop/portfolio';
const PUBLIC_PROJECTS_DIR = path.join(WORKSPACE_DIR, 'public', 'projects');
const OUTPUT_FILE = path.join(WORKSPACE_DIR, 'src', 'projectsData.ts');

const BLACKLIST = new Set([
  'public',
  'src',
  'node_modules',
  'dist',
  '.git',
  '.gemini',
  '.agents',
  '.vscode',
  '.DS_Store'
]);

// Desired order of projects as requested by the user
const DESIRED_ORDER = [
  'michela-donnino',
  'salvatore-manuele-iervolino',
  'austria',
  'austria-2',
  'snaps',
  'ferrigno',
  'zi-rosa',
  'ariete',
  'onemorepizza',
  'riva',
  'ugocrepa',
  'chyky',
  'indiemen'
];

function toTitleCase(str) {
  // Custom spelling for specific project titles
  const lower = str.toLowerCase();
  if (lower === 'riva') return 'Riva';
  if (lower === 'salvatore manuele iervolino') return 'Salvatore'; // User requested "Salvatore"
  if (lower === 'onemorepizza') return 'One More Pizza'; // User requested "One More Pizza"
  if (lower === 'ugocrepa') return 'Ugo Crepa'; // User requested "Ugo Crepa"
  return str
    .split(/[\s_-]+/)
    .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join(' ');
}

function getSlug(str) {
  return str
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)+/g, '');
}

function main() {
  // Ensure public/projects exists
  if (!fs.existsSync(PUBLIC_PROJECTS_DIR)) {
    fs.mkdirSync(PUBLIC_PROJECTS_DIR, { recursive: true });
    console.log(`Created directory: ${PUBLIC_PROJECTS_DIR}`);
  }

  // Scan public/projects for projects
  const projectsMap = {};
  if (fs.existsSync(PUBLIC_PROJECTS_DIR)) {
    const projectDirs = fs.readdirSync(PUBLIC_PROJECTS_DIR);
    for (const dir of projectDirs) {
      if (dir === '.DS_Store') continue;

      const dirPath = path.join(PUBLIC_PROJECTS_DIR, dir);
      if (fs.statSync(dirPath).isDirectory()) {
        const files = fs.readdirSync(dirPath);
        
        // Filter image files
        const images = files
          .filter(file => {
            const ext = path.extname(file).toLowerCase();
            return ['.jpg', '.jpeg', '.png', '.webp', '.gif', '.tiff'].includes(ext);
          })
          .sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' }))
          .map(file => `/projects/${dir}/${file}`);

        if (images.length > 0) {
          const id = getSlug(dir);
          const title = toTitleCase(dir);
          
          // Determine custom cover image
          let coverImage = images[0];
          if (id === 'ugocrepa') {
            coverImage = '/projects/ugocrepa/DSC08741.jpg';
          } else if (id === 'indiemen') {
            coverImage = '/projects/indiemen/DSC07262.jpg';
          } else if (id === 'salvatore-manuele-iervolino') {
            coverImage = '/projects/Salvatore Manuele Iervolino/DSC07979.jpg';
          } else if (id === 'austria-2') {
            coverImage = '/projects/austria 2/R0002100.jpg';
          } else if (id === 'onemorepizza') {
            coverImage = '/projects/onemorepizza/DSC03689.jpg';
          } else if (id === 'zi-rosa') {
            coverImage = '/projects/Zi rosa/DSC02601.jpg';
          }

          projectsMap[id] = {
            id,
            title,
            coverImage,
            images
          };
        }
      }
    }
  }

  // Sort projects according to DESIRED_ORDER
  const sortedProjects = [];
  for (const slug of DESIRED_ORDER) {
    if (projectsMap[slug]) {
      sortedProjects.push(projectsMap[slug]);
    } else {
      console.warn(`Warning: Project with slug "${slug}" not found.`);
    }
  }

  // Check if any projects exist in the map that were not in DESIRED_ORDER
  for (const slug in projectsMap) {
    if (!DESIRED_ORDER.includes(slug)) {
      sortedProjects.push(projectsMap[slug]);
      console.log(`Added project not in order list: "${projectsMap[slug].title}"`);
    }
  }

  // Ensure src/ directory exists
  const srcDir = path.dirname(OUTPUT_FILE);
  if (!fs.existsSync(srcDir)) {
    fs.mkdirSync(srcDir, { recursive: true });
  }

  // Generate src/projectsData.ts content
  const content = `// This file is auto-generated by setup-projects.js. Do not edit manually.

export interface Project {
  id: string;
  title: string;
  coverImage: string;
  images: string[];
}

export const projects: Project[] = ${JSON.stringify(sortedProjects, null, 2)};
`;

  fs.writeFileSync(OUTPUT_FILE, content, 'utf8');
  console.log(`Successfully generated sorted projects data: ${OUTPUT_FILE}`);
}

main();
